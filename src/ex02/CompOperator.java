package ex02;

public class CompOperator {
    public static void main(String[] args) {
        System.out.println((3 == 4) + " ");//false
        System.out.println((3 != 4) + " ");//true
        System.out.println((3 > 4) + " ");//false
        System.out.println((4 > 3) + " ");//ture

        System.out.println((3 == 4 && 4 == 7) + " ");//false
        System.out.println((3 != 4) + " ");//true
    }
}
